public class UseSwitch {
    public static void main(String[] args) {
        int ch =1 ;
        switch (ch){
            case 1:{
                System.out.println("内容是1");
                break;
            }
            case 2:{
                System.out.println("内容是2");
                break;
            }
            case 3:{
                System.out.println("内容是3");
                break;
            }
            case 4:{
                System.out.println("内容是4");
                break;
            }
            default:{
                System.out.println("没有匹配内容");
                break;
            }
        }
    }
}
